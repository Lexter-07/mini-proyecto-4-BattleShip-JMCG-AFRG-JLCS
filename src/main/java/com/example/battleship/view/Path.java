package com.example.battleship.view;


/**
 * Utility class that stores the paths to all FXML views used in the application.
 * .
 * This class centralizes the location of UI resources, making it easier
 * to manage and update scene paths without hardcoding them throughout
 * the project.
 * .
 * Each constant represents a specific view of the Battle Ship game:
 * - Game view
 * - Menu view
 * - ...
 */

public class Path {
    public static final String menuView = "/com/example/battleship/FXML/menu.fxml";
    public static final String startGameView = "/com/example/battleship/FXML/startGame-view.fxml";
    public static final String attackView = "/com/example/battleship/FXML/attack-view.fxml";
}
